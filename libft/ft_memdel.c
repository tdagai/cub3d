#include "libft.h"

void	ft_memdel(void **ap)
{
	if (ap && *ap)
	{
		free(*ap);
		*ap = NULL;
	}
}

void	ft_memdel_gnl(void *ap)
{
	if (ap)
	{
		free(ap);
		ap = NULL;
	}
}

